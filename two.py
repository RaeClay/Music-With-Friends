int1 = input('Give one integer')
int2 = input('Give me another')

if int(int1) == int(int2):
    print(str(int1) + ' and ' + str(int2) + ' are equal')
if int(int1) > int(int2):
    print(str(int1) + ' is bigger than ' + str(int2))
if int(int1) < int(int2):
    print(str(int1) + ' is less than ' + str(int2))